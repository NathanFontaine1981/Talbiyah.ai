/**
 * Check if an email address uses a disposable/temporary email domain
 */
export function isDisposableEmail(email: string): boolean {
  if (!email || typeof email !== 'string') return false;

  const domain = email.toLowerCase().split('@')[1];
  if (!domain) return false;

  return DISPOSABLE_DOMAINS.has(domain);
}

/**
 * Check if an email looks obviously fake
 * (e.g., test@test.com, fake@fake.com, asdf@asdf.com)
 */
export function isObviouslyFakeEmail(email: string): boolean {
  if (!email || typeof email !== 'string') return false;

  const lowerEmail = email.toLowerCase();

  // Check for obvious test patterns
  const fakePatterns = [
    /^test@test\./,
    /^fake@fake\./,
    /^asdf@asdf\./,
    /^qwerty@/,
    /^aaa+@/,
    /^123@/,
    /^abc@abc\./,
    /^noemail@/,
    /^none@/,
    /^null@/,
    /^dummy@/,
    /^sample@sample\./,
    /^example@example\./,
    /^user@user\./,
    /^admin@admin\./,
    /^no@no\./,
    /^na@na\./,
    /^x@x\./,
  ];

  return fakePatterns.some(pattern => pattern.test(lowerEmail));
}

/**
 * Validate email format
 */
export function isValidEmailFormat(email: string): boolean {
  if (!email || typeof email !== 'string') return false;

  // Basic email regex - not too strict but catches obvious issues
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return emailRegex.test(email);
}

/**
 * Comprehensive email validation
 * Returns { valid: boolean, error?: string }
 */
export function validateEmail(email: string): { valid: boolean; error?: string } {
  if (!email || !email.trim()) {
    return { valid: false, error: 'Email address is required' };
  }

  const trimmedEmail = email.trim().toLowerCase();

  if (!isValidEmailFormat(trimmedEmail)) {
    return { valid: false, error: 'Please enter a valid email address' };
  }

  if (isDisposableEmail(trimmedEmail)) {
    return { valid: false, error: 'Temporary or disposable email addresses are not allowed. Please use a permanent email address.' };
  }

  if (isObviouslyFakeEmail(trimmedEmail)) {
    return { valid: false, error: 'Please enter a real email address' };
  }

  return { valid: true };
}
